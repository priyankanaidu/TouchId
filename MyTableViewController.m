@interface MyTableViewController () <UIViewControllerPreviewingDelegate>

@end

@implementation MyTableViewController


- (void)viewDidLoad {
    [super viewDidLoad];
    
   
    if (self.traitCollection.forceTouchCapability) {
        [self registerForPreviewingWithDelegate:self sourceView:self.tableView];
    }
    
    // Uncomment the following line to preserve selection between presentations.
    // self.clearsSelectionOnViewWillAppear = NO;
    
    // Uncomment the following line to display an Edit button in the navigation bar for this view controller.
    // self.navigationItem.rightBarButtonItem = self.editButtonItem;
}


- (nullable UIViewController *)previewingContext:(id <UIViewControllerPreviewing>)previewingContext viewControllerForLocation:(CGPoint)location {
    
    NSIndexPath *indexPath = [self.tableView indexPathForRowAtPoint:location];
    
    CustomTableViewCell *cell = [self.tableView cellForRowAtIndexPath:indexPath];
    
    DetailViewController *dvc = [self.storyboard instantiateViewControllerWithIdentifier:@"detail"];
    
    dvc.detailText = cell.cellTextView.text;
    dvc.image = cell.cellImage.image;
    dvc.mytableController = self;
    
    
    return dvc;
}
- (void)previewingContext:(id <UIViewControllerPreviewing>)previewingContext commitViewController:(UIViewController *)viewControllerToCommit {
    
    [self showViewController:viewControllerToCommit sender:self];
}


- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

